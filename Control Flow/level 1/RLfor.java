package ass3;



	import java.util.*;

	public class RLfor {
	    public static void main(String[] args) {
	        Scanner sc = new Scanner(System.in);

	     
	        int counter = sc.nextInt();
	        sc.close();

	       
	        for (int i = counter; i >= 1; i--) {
	            System.out.println(i);
	        }

	        System.out.println("Launch!");
	    }
	}
